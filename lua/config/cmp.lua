local ok_cmp, cmp_package = pcall(require, 'cmp')
if not ok_cmp then
  print('No cmp plugin found')
  return
end

local ok_luansip, luasnip = pcall(require, 'luasnip')
if not ok_luansip then
  print('No luasnip plugin found')
  return
end

local cmp = cmp_package or {}

